'use strict'


//bucle for ;

 function main(i){
    for (var i ; i>= 0; i--){
        console.log(i);
    }
}

var contador = 30;
main(contador);
console.log(contador);

//Bucle while

while(contador >= 0){
    console.log(contador);
    contador--;
}

// do while
var contador = 20;
do {
    console.log(contador);
    contador--;
}while(contador >= 0);


